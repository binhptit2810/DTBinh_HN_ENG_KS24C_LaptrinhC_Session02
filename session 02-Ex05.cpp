#include <stdio.h>

int main(){
	// Khai bao chieu dai cua hinh chu nhat
	int chieudai = 15;
	
	// Khai bao chieu rong cua hinh chu nhat
	int chieurong = 5;
	
	// Khai bao chu vi cua hinh chu nhat
	int chuvi = (chieudai + chieurong) * 2;
	
	// Khai bao dien tich cua hinh chu nhat
	int dientich = chieudai * chieurong;
	
	// Chu vi cua hinh chu nhat la
	printf("Chu vi cua hinh chu nhat la:%d\n", chuvi);
	
	// Dien tich cua hinh chu nhat la
	printf("Dien tich cua hinh chu nhat la:%d\n", dientich);
	
	return 0;
	
}
