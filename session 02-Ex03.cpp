#include <stdio.h>

int main() {
	// Khai bao so nguyen thu nhat
	int firstNumber = 20;
	
	// Khai bao so nguyen thu hai
	int secondNumber = 10;
	
	// Khai bao tong cua hai so nguyen
	int tong = firstNumber + secondNumber;
	
	// Khai bao hieu cua hai so nguyen
	int hieu = firstNumber - secondNumber;
	
	// Khai bao tich cua hai so nguyen
	int tich = firstNumber * secondNumber;
	
	// Khai bao thuong cua hai so nguyen
	int thuong = firstNumber / secondNumber;
	
	// Tong cua hai so nguyen la
	printf ("Tong cua hai so nguyen la:%d\n", tong);
	
	// Hieu cua hai so nguyen la
	printf ("Hieu cua hai so nguyen la:%d\n", hieu);
	
	// Tich cua hai so nguyen la
	printf ("Tich cua hai so nguyen la:%d\n", tich);
	
	// Thuong cua hai so nguyen la
	printf ("Thuong cua hai so nguyen la:%d\n", thuong);
	
	return 0;
	
}
