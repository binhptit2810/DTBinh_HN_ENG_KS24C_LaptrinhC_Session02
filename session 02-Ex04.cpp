#include <stdio.h>

int main() {
	// Khai bao bien canh cua hinh vuong
	int canh = 10;
	
	// Khai bao chu vi cua hinh vuong
	int chuvi = canh * 4;
	
	// Khai bao dien tich cua hinh vuong 
	int dientich = canh * canh;
	
	// Chu vi cua hinh vuong la
	printf("Chu vi cua hinh vuong la:%d\n", chuvi);
	
	// Dien tich cua hinh vuong la
	printf("Dien tich cua hinh vuong la:%d\n", dientich);
	
	return 0;
}
