#include <stdio.h>

int main() {
	// Khai bao so nguyen thu nhat
	int firstNumber = 10;
	
	// Khai bao so nguyen thu hai
	int secondNumber = 20;
	
	// Khai bao tong cua hai so nguyen
	int total = firstNumber + secondNumber;
	
	// Tong cua hai so nguyen
	printf ("Tong cua hai so nguyen la: %d\n", total);
	
	return 0;
	
}
